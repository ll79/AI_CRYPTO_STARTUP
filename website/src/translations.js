// Function to update content based on selected language
function updateContent() {
    // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —è–∑—ã–∫ –¥–ª—è HTML —Ç–µ–≥–∞
    document.documentElement.lang = currentLanguage;
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç—Ä–∞–Ω–∏—Ü—ã –∏ –º–µ—Ç–∞-—Ç–µ–≥–∏
    if (currentLanguage === 'ru') {
        document.title = "ASINGULARITY AI | –î–µ—Ü–µ–Ω—Ç—Ä–∞–ª–∏–∑–æ–≤–∞–Ω–Ω–∞—è –ò–ò-—ç–∫–æ—Å–∏—Å—Ç–µ–º–∞";
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) {
            metaDesc.setAttribute('content', '–†–µ–≤–æ–ª—é—Ü–∏–æ–Ω–Ω–∞—è –¥–µ—Ü–µ–Ω—Ç—Ä–∞–ª–∏–∑–æ–≤–∞–Ω–Ω–∞—è —ç–∫–æ—Å–∏—Å—Ç–µ–º–∞, –æ–±—ä–µ–¥–∏–Ω—è—é—â–∞—è –ø–µ—Ä–µ–¥–æ–≤—ã–µ —Ç–µ—Ö–Ω–æ–ª–æ–≥–∏–∏ –ò–ò, –ö—Ä–∏–ø—Ç–æ, NFT –∏ Web3 –Ω–∞ –±–ª–æ–∫—á–µ–π–Ω–µ TON');
        }
    } else {
        document.title = "ASINGULARITY AI | Decentralized AI Ecosystem";
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) {
            metaDesc.setAttribute('content', 'Building a revolutionary ecosystem that combines AI, Crypto, NFT, and Web3 technologies on TON blockchain');
        }
    }
    
    // –ù–∞—Ö–æ–¥–∏–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å –∞—Ç—Ä–∏–±—É—Ç–æ–º data-i18n –∏ –æ–±–Ω–æ–≤–ª—è–µ–º –∏—Ö —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            // –û–±—Ä–∞–±–æ—Ç–∫–∞ —Ä–∞–∑–Ω—ã—Ö —Ç–∏–ø–æ–≤ —ç–ª–µ–º–µ–Ω—Ç–æ–≤
            const tagName = element.tagName.toUpperCase();
            
            if (tagName === 'INPUT') {
                // –î–ª—è –ø–æ–ª–µ–π –≤–≤–æ–¥–∞
                if (element.type === 'text' || element.type === 'email' || element.type === 'search') {
                    element.setAttribute('placeholder', translations[currentLanguage][key]);
                } else {
                    element.value = translations[currentLanguage][key];
                }
            } else if (tagName === 'META') {
                // –î–ª—è –º–µ—Ç–∞-—ç–ª–µ–º–µ–Ω—Ç–æ–≤
                element.setAttribute('content', translations[currentLanguage][key]);
            } else if (tagName === 'A') {
                // –î–ª—è —Å—Å—ã–ª–æ–∫
                if (element.hasAttribute('title')) {
                    element.setAttribute('title', translations[currentLanguage][key]);
                }
                // –ï—Å–ª–∏ —É —Å—Å—ã–ª–∫–∏ –Ω–µ—Ç –¥–æ—á–µ—Ä–Ω–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤, –æ–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç
                if (element.childElementCount === 0) {
                    element.textContent = translations[currentLanguage][key];
                }
            } else if (tagName === 'IMG' || tagName === 'IFRAME') {
                // –î–ª—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π –∏ iframe
                element.setAttribute('alt', translations[currentLanguage][key]);
            } else {
                // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ —Å–ª—É—á–∞–∏ –¥–ª—è —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å data-text –∞—Ç—Ä–∏–±—É—Ç–æ–º (–¥–ª—è glitch-—ç—Ñ—Ñ–µ–∫—Ç–∞)
                if (element.hasAttribute('data-text')) {
                    element.setAttribute('data-text', translations[currentLanguage][key]);
                }
                
                // –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Ç–µ–∫—Å—Ç–æ–≤–æ–≥–æ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ –¥–ª—è –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤
                element.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –≥–ª—é–∫-—ç—Ñ—Ñ–µ–∫—Ç –ø—Ä–∏ —Å–º–µ–Ω–µ —è–∑—ã–∫–∞
    document.querySelectorAll('.glitch-text, .glitch-hover').forEach(element => {
        element.classList.add('active-glitch');
        setTimeout(() => {
            element.classList.remove('active-glitch');
        }, 1000);
    });
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∞–∫—Ç–∏–≤–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∫–Ω–æ–ø–æ–∫ –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —è–∑—ã–∫–∞
    document.querySelectorAll('.language-switcher button').forEach(btn => {
        if (btn.getAttribute('data-lang') === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

